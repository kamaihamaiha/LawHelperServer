// resolveLocation 根据 authorityName 解析出省级行政区和市/州名称
// 返回 (province, city)，用于客户端 省→市→机关 三级联动选择
func resolveLocation(authorityName string) (province, city string) {
	name := strings.TrimSpace(authorityName)
	if name == "" {
		return "", ""
	}

	// 优先级 1：直辖市（北京/上海/天津/重庆）
	for _, m := range municipalityPrefixes {
		if strings.HasPrefix(name, m) {
			return m, m
		}
	}

	// 优先级 2：省名前缀匹配（如"广东省人民代表大会"→province="广东省"）
	for _, p := range provincePrefixes {
		if strings.HasPrefix(name, p) {
			return p, ""
		}
	}

	// 优先级 3：自治区名前缀匹配
	for _, p := range autonomousRegionPrefixes {
		if strings.HasPrefix(name, p) {
			return p, ""
		}
	}

	// 优先级 4：自治州（如"凉山彝族自治州"）
	for _, az := range autonomousPrefectureNames {
		if strings.HasPrefix(name, az) {
			p := cityToProvinceMap[az]
			return p, az
		}
	}

	// 优先级 5：自治县/自治旗/县 → 查 countyToParentMap
	for prefix, loc := range countyToParentMap {
		if strings.HasPrefix(name, prefix) {
			return loc.province, loc.city
		}
	}

	// 优先级 6：地级市（如"深圳市人民代表大会"→提取"深圳市"查 cityToProvinceMap）
	for _, suffix := range citySuffixes {
		if idx := strings.Index(name, suffix); idx > 0 {
			cityName := name[:idx+len(suffix)]
			if p, ok := cityToProvinceMap[cityName]; ok {
				return p, cityName
			}
		}
	}

	return "", ""
}

